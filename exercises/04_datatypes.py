# We can check something's data type with the type() function.

print('These are integers:')
type(123)
type(33)
type(2 + 5)

print('These are boolean (true or false) values:')
type(True)
type(False)
type(True == False)

print('These are floating-point values:')
type(24.12)
type(562.124432 - 2.9)

print('These are strings')
type('this is a string')
type('also a string')
type('x')